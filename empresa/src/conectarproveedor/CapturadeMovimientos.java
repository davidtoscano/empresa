    private void jTextFieldcantidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldcantidadKeyTyped
  //////// Valida si es numero en el campor CANTIDAD/////////////////////////////
               
       int k=(int)evt.getKeyChar();
       if (k >= 97 && k <= 122 || k>=65 && k<=90){
       evt.setKeyChar((char)KeyEvent.VK_CLEAR);
       
       JOptionPane.showMessageDialog(null,"Caracter No valido","Ventana Error Datos",JOptionPane.ERROR_MESSAGE);
   }
       if(k==241 || k==209){
           
      evt.setKeyChar((char)KeyEvent.VK_CLEAR);
      JOptionPane.showMessageDialog(null,"Caracter No valido","Ventana Error Datos",JOptionPane.ERROR_MESSAGE);
}
if(k==10){
         jTextFieldcantidad.transferFocus();
}

    }//GEN-LAST:event_jTextFieldcantidadKeyTyped

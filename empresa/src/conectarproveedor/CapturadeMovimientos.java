    private void jbtnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnguardarActionPerformed

        try {
            //Instancias nuestra clase conectar
            conectar miconexion = new conectar();
            //Reemplaza valores 
            String sentenciaInsert = "insert into movimientos values (?,?,?,?,?,?,?)";
            miconexion.prepararSentencias = miconexion.miconexion.prepareStatement(sentenciaInsert);

            miconexion.prepararSentencias.setString(1, jTextFieldnumero.getText());
            miconexion.prepararSentencias.setString(2, jTextFieldclave.getText());
            miconexion.prepararSentencias.setString(3, jTextFielddescripcion.getText());           
            miconexion.prepararSentencias.setString(4, jTextFieldunidad.getText());
            miconexion.prepararSentencias.setString(5, jTextFieldcantidad.getText());
            miconexion.prepararSentencias.setString(6, jTextFieldprecio.getText());
            miconexion.prepararSentencias.setString(7, jTextFieldtotal.getText());

            miconexion.prepararSentencias.executeUpdate();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jbtnguardarActionPerformed

